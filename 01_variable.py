# numeric datatypes
i = 13  # integer
f = 12.4  # float
c = complex(3, 4) # c = 3+4j
print("numeric datatypes")
print(type(i))
print(type(f))
print(type(c))

print("text datatypes")
s = "w"  # string
print(type(s))

print("\nsequence datatypes ")
lst = [1, 2, 3, 4, 5]  # list
st = {3, 4, 5}  # set
tpl = (1, 2, 3, 4)
print(type(lst))
print(type(st))
print(type(tpl))


print("boolean datatypes")

b = True
print(type(b))

r = range(4,400)
print(r)
print(type(r))

# None datatype
n = None
print(type(n))
