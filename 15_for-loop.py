# loops are used to repeat a certain task  for a particular time
# print from 1 to 5
# for i in range(start_index,end_index(excluded))
for i in range(1, 6):
    print(i) #this will print from 1 to 5

# for i in range(end_index(excluded)) //starts from 0
for i in range(6):
    print(i) #this will print from 0 to 5


# using loops in lists
lst = [1, 4, 5, 657, 3, 2, 324]
for it in lst:
    print(it)

